def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _append_i18n_translate_trace(payload: dict[str, object]) -> None:
    if not isinstance(payload, dict):
        return
    row = dict(payload)
    row["ts"] = str(row.get("ts") or _now_iso())
    try:
        I18N_TRANSLATE_TRACE_PATH.parent.mkdir(parents=True, exist_ok=True)
        line = json.dumps(row, ensure_ascii=False)
    except Exception:
        return
    try:
        with I18N_TRACE_LOCK:
            with I18N_TRANSLATE_TRACE_PATH.open("a", encoding="utf-8") as fh:
                fh.write(line + "\n")
    except Exception:
        return


def _normalize_lang_tag(lang: str | None) -> str:
    tag = str(lang or "zh-Hant").strip()
    normalized = tag.lower().replace("_", "-")
    if normalized in {"zh", "zh-tw", "zh-hant", "tw", "traditional", "繁中"}:
        return "zh-Hant"
    if normalized in {"zh-cn", "zh-hans", "cn", "simplified", "简中", "簡中"}:
        return "zh-Hans"
    if normalized in {"en", "en-us", "english"}:
        return "en"
    if normalized in {"ko", "ko-kr", "kr", "korean"}:
        return "ko"
    return tag if tag in {"zh-Hant", "zh-Hans", "en", "ko"} else "zh-Hant"


